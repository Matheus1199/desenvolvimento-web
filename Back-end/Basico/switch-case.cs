using System;

public class Program
{
    public static void Main(String[] args)
    {
        int caseSwitch = 1;

        switch (caseSwitch)
        {
            case 1:
                console.WriteLine("Case 1");
                break;
            case 2:
                console.WriteLine("Case 2");
                break;
            default:
                console.WriteLine("Default Case");
                break;
        }
    }
}

